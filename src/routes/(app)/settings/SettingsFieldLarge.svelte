<script lang="ts">
  import { createEventDispatcher } from "svelte";

  interface Option {
    name: string;
    value: string;
  }

  export let title: string;
  export let type: "checkbox" | "toggle" | "select" | "text" | "button" | "link";
  export let value: string = "";
  export let options: Option[] = [];
  export let maxlength: number | undefined = undefined;
  export let placeholder: string = "";
  export let text: string = "";
  export let href: string = "";

  let checkbox: HTMLInputElement;

  $: value,
    (() => {
      if (checkbox) {
        checkbox.checked = value === "true";
      }
    })();

  const dispatch = createEventDispatcher();
</script>

<div class="flex flex-col gap-2 items-center rounded-box border px-4 py-3">
  <div class="flex flex-col items-center text-center">
    <p class="text-2xl"><slot name="icon"></slot></p>
    <h3>{title}</h3>
    <p class="text-sm text-base-content/50"><slot></slot></p>
  </div>
  {#if type === "checkbox" || type === "toggle"}
    <input
      type="checkbox"
      class={type === "checkbox" ? "checkbox rounded-md" : "toggle"}
      bind:this={checkbox}
      on:input={() => {
        value = checkbox.checked ? "true" : "false";
      }}
    />
  {:else if type === "select"}
    <select class="select select-bordered select-sm w-fit" bind:value>
      {#each options as option}
        <option value={option.value}>{option.name}</option>
      {/each}
    </select>
  {:else if type === "text"}
    <div class="flex flex-row gap-1">
      <input
        type="text"
        class="input input-bordered input-sm w-full max-w-xs"
        bind:value
        {maxlength}
        {placeholder}
      />
      <button
        class="btn btn-sm"
        on:click={() => {
          dispatch("save", {
            value,
          });
        }}
        >Save
      </button>
    </div>
  {:else if type === "button"}
    <button class="btn btn-sm" on:click>{text}</button>
  {:else if type === "link"}
    <a class="btn btn-sm" {href}>{text}</a>
  {/if}
</div>
